func TestUniq(t *testing.T) {
	assert.Equal(t, []string{"a", "b", "c"}, uniq([]string{"a", "b", "c", "b", "a"}))
	assert.Nil(t, uniq(nil))
	assert.Equal(t, []string{}, uniq([]string{}))
	assert.Equal(t, []string{"a"}, uniq([]string{"a"}))
}

